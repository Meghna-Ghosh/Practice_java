public class chk {
    public static void main(String[] args) {
        int i=0;
        for(i=0;i>=0;i++) {
           System.out.println(i); 
        }

        
        while(i>=0) {
            System.out.println(i);
            i++;
        }


        do{
            System.out.println(i);
            i++;
        } while(i>=0);
    }
    
}
